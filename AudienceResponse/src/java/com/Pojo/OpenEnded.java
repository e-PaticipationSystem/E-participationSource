package com.Pojo;

/**
 *
 * @author EDU-Admin
 */
public class OpenEnded {
    private int OpenEndedId;
    private String Question;
    private int BallotId;

    /**
     * @return the OpenEndedId
     */
    public int getOpenEndedId() {
        return OpenEndedId;
    }

    /**
     * @param OpenEndedId the OpenEndedId to set
     */
    public void setOpenEndedId(int OpenEndedId) {
        this.OpenEndedId = OpenEndedId;
    }

    /**
     * @return the Question
     */
    public String getQuestion() {
        return Question;
    }

    /**
     * @param Question the Question to set
     */
    public void setQuestion(String Question) {
        this.Question = Question;
    }

    /**
     * @return the BallotId
     */
    public int getBallotId() {
        return BallotId;
    }

    /**
     * @param BallotId the BallotId to set
     */
    public void setBallotId(int BallotId) {
        this.BallotId = BallotId;
    }
}
